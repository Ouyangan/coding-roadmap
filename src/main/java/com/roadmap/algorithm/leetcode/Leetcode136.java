public class Leetcode136 {
    /**
     * a^a=0;
     * a^a^b=b;
     *
     * @param nums
     * @return
     */
    public int singleNumber(int[] nums) {
        int target = 0;
        for (int num : nums) {
            target = target ^ num;
        }
        return target;
    }
}
