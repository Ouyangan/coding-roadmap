import java.util.PriorityQueue;

public class Leetcode215 {
    public int findKthLargest(int[] nums, int k) {
        int len = nums.length;
        PriorityQueue<Integer> queue = new PriorityQueue<>(k);
        for (int i = 0; i < k; i++) {
            queue.add(nums[i]);
        }
        for (int i = k; i < len; i++) {
            int top = queue.peek();
            if (nums[i] > top) {
                queue.poll();
                queue.add(nums[i]);
            }
        }
        return queue.peek();
    }
}
